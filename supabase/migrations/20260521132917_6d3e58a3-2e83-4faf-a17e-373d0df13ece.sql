
-- L1 — Policy Governance Runtime

CREATE TABLE IF NOT EXISTS public.policy_versions (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  version_no bigint GENERATED BY DEFAULT AS IDENTITY,
  parent_version_id uuid REFERENCES public.policy_versions(id),
  change_kind text NOT NULL,
  reason text NOT NULL DEFAULT 'auto',
  created_by text NOT NULL DEFAULT 'service_role',
  created_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT policy_versions_change_kind_chk CHECK (change_kind IN
    ('seed','ema_adjust','manual_override','rollback','disable','enable'))
);

CREATE TABLE IF NOT EXISTS public.policy_change_audits (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  version_id uuid REFERENCES public.policy_versions(id) ON DELETE CASCADE,
  policy_key text NOT NULL,
  before_weight numeric,
  after_weight numeric,
  delta numeric GENERATED ALWAYS AS (COALESCE(after_weight,0) - COALESCE(before_weight,0)) STORED,
  sample_size int,
  positive_rate_pct numeric,
  source text NOT NULL DEFAULT 'fn_apply_policy_mutation_bounded',
  recorded_at timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_policy_change_audits_key_time
  ON public.policy_change_audits (policy_key, recorded_at DESC);

CREATE TABLE IF NOT EXISTS public.policy_effectiveness_windows (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  version_id uuid REFERENCES public.policy_versions(id) ON DELETE CASCADE,
  window_start timestamptz NOT NULL,
  window_end timestamptz,
  metric text NOT NULL,
  before_value numeric,
  after_value numeric,
  delta numeric GENERATED ALWAYS AS (COALESCE(after_value,0) - COALESCE(before_value,0)) STORED,
  sample_size int,
  notes text
);
CREATE INDEX IF NOT EXISTS idx_policy_effectiveness_version_metric
  ON public.policy_effectiveness_windows (version_id, metric);

CREATE TABLE IF NOT EXISTS public.policy_rollback_snapshots (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  version_id uuid NOT NULL REFERENCES public.policy_versions(id) ON DELETE CASCADE,
  snapshot jsonb NOT NULL,
  taken_at timestamptz NOT NULL DEFAULT now()
);


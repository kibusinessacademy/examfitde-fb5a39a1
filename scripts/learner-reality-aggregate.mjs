#!/usr/bin/env node
/**
 * Learner-Reality Daily QA Aggregator.
 *
 * Reads tests/customer-reality/learner outputs from `reality-results/`:
 *   - journey-pass/*.json   (one per journey, status pass|fail)
 *   - findings/*.json       (P0/P1/P2)
 *   - learner-login-success.flag  (must exist for RELEASE)
 *
 * Emits:
 *   - reality-results/learner-reality-results.json
 *   - reality-results/learner-reality-report.md
 *
 * Exit codes:
 *   0  → RELEASE
 *   1  → REVIEW
 *   2  → BLOCK
 */
import fs from 'node:fs';
import path from 'node:path';

const RESULTS_DIR = path.resolve(process.cwd(), 'reality-results');
const PASS_DIR = path.join(RESULTS_DIR, 'journey-pass');
const FIND_DIR = path.join(RESULTS_DIR, 'findings');
const LOGIN_FLAG = path.join(RESULTS_DIR, 'learner-login-success.flag');

const WEIGHTS = {
  J01_discovery: 10,
  J02_account: 10,
  J03_purchase: 10,
  J04_onboarding: 10,
  J05_learning: 15,
  J06_minicheck: 10,
  J07_ai_tutor: 10,
  J08_written_exam: 10,
  J09_oral_exam: 10,
  J10_return: 5,
  // Hard P0-Guard: globaler TOTP/Security-Blocker darf nicht durchrutschen.
  J11_navigation_no_totp_blocker: 15,
};

function readDir(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs.readdirSync(dir)
    .filter((f) => f.endsWith('.json'))
    .map((f) => {
      try { return JSON.parse(fs.readFileSync(path.join(dir, f), 'utf8')); }
      catch { return null; }
    })
    .filter(Boolean);
}

const passes = readDir(PASS_DIR);
const findings = readDir(FIND_DIR);

const journeyStatus = {};
for (const id of Object.keys(WEIGHTS)) journeyStatus[id] = 'missing';
for (const p of passes) if (WEIGHTS[p.id] !== undefined) journeyStatus[p.id] = p.status;

let score = 0;
for (const [id, w] of Object.entries(WEIGHTS)) {
  if (journeyStatus[id] === 'pass') score += w;
}

const p0 = findings.filter((f) => f.severity === 'P0');
const p1 = findings.filter((f) => f.severity === 'P1');
const p2 = findings.filter((f) => f.severity === 'P2');

const loginOk = fs.existsSync(LOGIN_FLAG);

let status;
if (!loginOk) {
  status = 'BLOCK';
} else if (p0.length > 0 || score < 70) {
  status = 'BLOCK';
} else if (p1.length > 0 || score < 85) {
  status = 'REVIEW';
} else {
  status = 'RELEASE';
}

const baseUrl =
  process.env.REALITY_BASE_URL ||
  process.env.PREVIEW_URL ||
  'https://examfitde.lovable.app';

const result = {
  ts: new Date().toISOString(),
  base_url: baseUrl,
  status,
  score,
  max_score: Object.values(WEIGHTS).reduce((a, b) => a + b, 0),
  login_validated: loginOk,
  journeys: journeyStatus,
  weights: WEIGHTS,
  findings: { p0, p1, p2 },
  counts: { p0: p0.length, p1: p1.length, p2: p2.length, findings: findings.length },
};

fs.mkdirSync(RESULTS_DIR, { recursive: true });
fs.writeFileSync(
  path.join(RESULTS_DIR, 'learner-reality-results.json'),
  JSON.stringify(result, null, 2),
);

// Markdown report
const passList = Object.entries(journeyStatus)
  .filter(([, s]) => s === 'pass')
  .map(([id]) => `- ✅ ${id} (${WEIGHTS[id]} pts)`)
  .join('\n') || '- _(none)_';
const failList = Object.entries(journeyStatus)
  .filter(([, s]) => s !== 'pass')
  .map(([id, s]) => `- ❌ ${id} (${WEIGHTS[id]} pts) — ${s}`)
  .join('\n') || '- _(none)_';

const fmtFinding = (f) =>
  `- **${f.severity} / ${f.kind}** — \`${f.journey}\` ${f.route ? `\`${f.route}\`` : ''}\n  - ${f.detail}\n  ${f.fix ? `- _Fix:_ ${f.fix}` : ''}`;

const md = `# Learner Reality Daily QA — ${result.ts.slice(0, 10)}

**Base URL:** ${baseUrl}
**Status:** **${status}**
**Customer Readiness Score:** **${score} / ${result.max_score}**
**Login validated:** ${loginOk ? '✅ yes' : '❌ NO — release impossible'}

## Journeys passed
${passList}

## Journeys failed / missing
${failList}

## Findings
**Counts:** P0=${p0.length} · P1=${p1.length} · P2=${p2.length} · total=${findings.length}

### P0 (blockers)
${p0.length ? p0.map(fmtFinding).join('\n') : '_none_'}

### P1 (trust / conversion)
${p1.length ? p1.map(fmtFinding).join('\n') : '_none_'}

### P2 (UX friction)
${p2.length ? p2.map(fmtFinding).join('\n') : '_none_'}

## Recommended fix order
${[...p0, ...p1, ...p2].slice(0, 10).map((f, i) => `${i + 1}. **${f.severity}** ${f.kind} → ${f.detail}`).join('\n') || '_no findings_'}

---
_Generated by scripts/learner-reality-aggregate.mjs — Reality QA, not architecture._
`;

fs.writeFileSync(path.join(RESULTS_DIR, 'learner-reality-report.md'), md);

// Console summary
console.log(`\n=== Learner Reality QA — ${status} (${score}/${result.max_score}) ===`);
console.log(`Login validated: ${loginOk}`);
console.log(`P0=${p0.length} P1=${p1.length} P2=${p2.length}`);
console.log(`Report: reality-results/learner-reality-report.md`);

process.exit(status === 'RELEASE' ? 0 : status === 'REVIEW' ? 1 : 2);

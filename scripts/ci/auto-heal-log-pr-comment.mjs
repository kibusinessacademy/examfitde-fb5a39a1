#!/usr/bin/env node
/**
 * Posts a PR review comment with the auto_heal_log migration runbook
 * (clustered checklist) plus a table of static violations with file/line
 * permalinks to the PR head.
 *
 * ENV (workflow):
 *   GITHUB_TOKEN, GITHUB_REPOSITORY, GITHUB_EVENT_PATH,
 *   GITHUB_HEAD_REF (or GITHUB_SHA)
 * Inputs:
 *   GUARD_JSON_PATH   path to JSON output of the guard (--json mode)
 *   RUNBOOK_PATH      path to generated runbook .md
 */
import { readFileSync, existsSync } from "node:fs";

const {
  GITHUB_TOKEN, GITHUB_REPOSITORY, GITHUB_EVENT_PATH,
  GITHUB_SERVER_URL = "https://github.com",
  GITHUB_SHA, GITHUB_HEAD_REF,
  GUARD_JSON_PATH, RUNBOOK_PATH,
} = process.env;

if (!GITHUB_TOKEN || !GITHUB_REPOSITORY || !GITHUB_EVENT_PATH) {
  console.log("ℹ️ Not in PR context — skipping comment.");
  process.exit(0);
}
const ev = JSON.parse(readFileSync(GITHUB_EVENT_PATH, "utf8"));
const prNumber = ev?.pull_request?.number;
if (!prNumber) { console.log("ℹ️ No PR number — skipping."); process.exit(0); }

const sha = ev?.pull_request?.head?.sha || GITHUB_SHA;
const refLink = (file, line) =>
  `${GITHUB_SERVER_URL}/${GITHUB_REPOSITORY}/blob/${sha}/${file}#L${line}`;

const guard = GUARD_JSON_PATH && existsSync(GUARD_JSON_PATH)
  ? JSON.parse(readFileSync(GUARD_JSON_PATH, "utf8"))
  : { staticViolations: [], live: { skipped: true } };

const runbook = RUNBOOK_PATH && existsSync(RUNBOOK_PATH)
  ? readFileSync(RUNBOOK_PATH, "utf8")
  : "_(no runbook generated)_";

const v = guard.staticViolations || [];
const violationTable = v.length === 0
  ? "✅ No static violations."
  : [
      "| File | Line | Forbidden columns |",
      "|------|------|-------------------|",
      ...v.map((x) => `| [\`${x.file}\`](${refLink(x.file, x.line)}) | ${x.line} | \`${x.columns.join(", ")}\` |`),
    ].join("\n");

const liveLine = guard.live?.skipped
  ? "_(live check skipped)_"
  : guard.live?.error
    ? `❌ Live error: \`${guard.live.error}\``
    : `Live offenders: **${guard.live?.offenders?.length ?? 0}** / ${guard.live?.total ?? 0}`;

const body = [
  `## 🩺 auto_heal_log Canonical Schema Guard`,
  ``,
  `${liveLine}`,
  ``,
  `### Static violations`,
  violationTable,
  ``,
  `### Migration runbook`,
  runbook,
  ``,
  `<sub>Auto-generated by \`scripts/ci/auto-heal-log-pr-comment.mjs\`</sub>`,
].join("\n");

const url = `https://api.github.com/repos/${GITHUB_REPOSITORY}/issues/${prNumber}/comments`;
const res = await fetch(url, {
  method: "POST",
  headers: {
    Authorization: `Bearer ${GITHUB_TOKEN}`,
    "Content-Type": "application/json",
    Accept: "application/vnd.github+json",
  },
  body: JSON.stringify({ body }),
});
if (!res.ok) {
  console.error(`❌ PR comment failed: ${res.status} ${await res.text()}`);
  process.exit(1);
}
console.log(`✅ PR #${prNumber} comment posted.`);

#!/usr/bin/env node
/**
 * Guard-Failure CI Reporter
 * ─────────────────────────
 * Postet einen kompakten, menschenlesbaren PR-Kommentar (oder Issue-Update)
 * wenn ein Guard im CI fehlschlägt — und legt zugleich ein Markdown-Artifact
 * `guard-report.md` ab, das per `actions/upload-artifact` mitgeschickt werden
 * kann.
 *
 * ENV (von Workflow gesetzt):
 *   GITHUB_TOKEN          - permission: pull-requests: write, issues: write
 *   GITHUB_REPOSITORY     - owner/repo
 *   GITHUB_EVENT_NAME     - "pull_request" | "schedule" | "workflow_dispatch" | ...
 *   GITHUB_REF            - z.B. refs/pull/123/merge
 *   GUARD_NAME            - z.B. "Persona Enum Contract"
 *   GUARD_LOG_PATH        - Datei mit stdout/stderr des Guards (optional)
 *   RUN_URL               - Link zum Workflow-Run
 *
 * Nutzung im Workflow:
 *   - name: Run guard
 *     id: g
 *     continue-on-error: true
 *     run: node scripts/persona-enum-contract.mjs | tee /tmp/guard.log
 *   - name: Report on failure
 *     if: steps.g.outcome == 'failure'
 *     env:
 *       GITHUB_TOKEN: ${{ secrets.GITHUB_TOKEN }}
 *       GUARD_NAME: "Persona Enum Contract"
 *       GUARD_LOG_PATH: /tmp/guard.log
 *       RUN_URL: ${{ github.server_url }}/${{ github.repository }}/actions/runs/${{ github.run_id }}
 *     run: node scripts/ci/guard-report-comment.mjs
 *   - uses: actions/upload-artifact@v4
 *     if: always()
 *     with:
 *       name: guard-report
 *       path: guard-report.md
 */
import { readFileSync, writeFileSync, existsSync } from "node:fs";

const {
  GITHUB_TOKEN,
  GITHUB_REPOSITORY,
  GITHUB_EVENT_NAME,
  GITHUB_REF,
  GUARD_NAME = "Unknown Guard",
  GUARD_LOG_PATH,
  RUN_URL = "",
} = process.env;

function readLog() {
  if (!GUARD_LOG_PATH || !existsSync(GUARD_LOG_PATH)) return "(no log captured)";
  const raw = readFileSync(GUARD_LOG_PATH, "utf8");
  // Last 80 lines, dedupe blanks.
  const lines = raw.split(/\r?\n/).filter(Boolean);
  return lines.slice(-80).join("\n");
}

function classify(log) {
  const hits = [];
  if (/forbidden legacy/i.test(log)) hits.push("Legacy-Wert in DB-Enum oder FE-Whitelist gefunden");
  if (/missing in db/i.test(log))   hits.push("Frontend referenziert Werte, die nicht im DB-Enum existieren");
  if (/unknown values vs fe/i.test(log)) hits.push("DB-Enum enthält Werte, die im Frontend fehlen (strict mode)");
  if (/drift/i.test(log) && hits.length === 0) hits.push("Generischer Drift erkannt");
  if (/HTTP\s+(4\d\d|5\d\d)/i.test(log)) hits.push("Backend-Request fehlgeschlagen (HTTP-Fehler)");
  if (/timeout/i.test(log)) hits.push("Timeout beim Backend-Call");
  return hits;
}

function buildBody(log, hints) {
  return [
    `## ❌ Guard-Fail: ${GUARD_NAME}`,
    ``,
    `**Workflow-Run:** ${RUN_URL || "(n/a)"}`,
    `**Event:** \`${GITHUB_EVENT_NAME}\` · **Ref:** \`${GITHUB_REF || "n/a"}\``,
    `**Zeit:** ${new Date().toISOString()}`,
    ``,
    hints.length > 0
      ? `### Wahrscheinliche Ursache\n${hints.map((h) => `- ${h}`).join("\n")}`
      : `### Wahrscheinliche Ursache\n- Unspezifischer Fail – Log unten prüfen.`,
    ``,
    `### Letzte Log-Zeilen`,
    "```",
    log,
    "```",
    ``,
    `### Nächste Schritte`,
    `1. Lokal reproduzieren: \`node ${GUARD_LOG_PATH ? "<scripts/...>" : "<guard-script>"}\``,
    `2. Bei DB-Drift: passende Migration anlegen oder Frontend-Whitelist anpassen.`,
    `3. Bei Forbidden-Wert: Migration zum Entfernen + Memory-Eintrag aktualisieren.`,
    ``,
    `<sub>Auto-generated by \`scripts/ci/guard-report-comment.mjs\`</sub>`,
  ].join("\n");
}

async function postPrComment(body) {
  if (!GITHUB_TOKEN || !GITHUB_REPOSITORY) {
    console.log("⚠️  GITHUB_TOKEN/REPOSITORY fehlt – nur Artifact-Datei wird geschrieben.");
    return;
  }
  // PR number aus GITHUB_REF (refs/pull/N/merge)
  const m = (GITHUB_REF || "").match(/refs\/pull\/(\d+)\//);
  if (!m) {
    console.log("ℹ️  Kein PR-Kontext (Schedule/Push) – Issue-Fallback via issue-on-fail.mjs separat nutzen.");
    return;
  }
  const prNumber = m[1];
  const url = `https://api.github.com/repos/${GITHUB_REPOSITORY}/issues/${prNumber}/comments`;
  const res = await fetch(url, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${GITHUB_TOKEN}`,
      "Content-Type": "application/json",
      Accept: "application/vnd.github+json",
    },
    body: JSON.stringify({ body }),
  });
  if (res.ok) console.log(`✅ PR-Kommentar gepostet: #${prNumber}`);
  else console.error(`❌ PR-Kommentar fehlgeschlagen: ${res.status} ${await res.text()}`);
}

const log = readLog();
const hints = classify(log);
const body = buildBody(log, hints);

writeFileSync("guard-report.md", body);
console.log("📝 guard-report.md geschrieben.");
await postPrComment(body);

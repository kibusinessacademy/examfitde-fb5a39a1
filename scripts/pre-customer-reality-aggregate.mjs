#!/usr/bin/env node
/**
 * Pre-Customer Reality Daily QA Aggregator.
 *
 * Liest tests/customer-reality/precustomer-Outputs aus reality-results/:
 *   - journey-pass/P0*.json   (status pass|fail)
 *   - findings/*.json         (P0/P1/P2)
 *   - pre-customer-metrics.json (TIME_TO_COURSE etc.)
 *
 * Emittiert:
 *   - reality-results/pre-customer-reality-results.json
 *   - reality-results/pre-customer-reality-report.md
 *
 * Exit-Codes: 0 RELEASE · 1 REVIEW · 2 BLOCK
 */
import fs from 'node:fs';
import path from 'node:path';

const RESULTS_DIR = path.resolve(process.cwd(), 'reality-results');
const PASS_DIR = path.join(RESULTS_DIR, 'journey-pass');
const FIND_DIR = path.join(RESULTS_DIR, 'findings');
const METRIC_FILE = path.join(RESULTS_DIR, 'pre-customer-metrics.json');

const WEIGHTS = {
  P01_homepage: 10,
  P02_find_beruf: 15,
  P03_open_course: 15,
  P04_pricing: 15,
  P05_cta_click: 10,
  P06_checkout_surface: 15,
  P07_cross_sell: 10,
  P08_berufos_hub: 10,
};
const TTC_TARGET_MS = 60_000;

function readDir(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs.readdirSync(dir)
    .filter((f) => f.endsWith('.json'))
    .map((f) => { try { return JSON.parse(fs.readFileSync(path.join(dir, f), 'utf8')); } catch { return null; } })
    .filter(Boolean);
}

const passes = readDir(PASS_DIR);
const findings = readDir(FIND_DIR);
let metrics = {};
if (fs.existsSync(METRIC_FILE)) {
  try { metrics = JSON.parse(fs.readFileSync(METRIC_FILE, 'utf8')); } catch { /* */ }
}

const journeyStatus = {};
for (const id of Object.keys(WEIGHTS)) journeyStatus[id] = 'missing';
for (const p of passes) if (WEIGHTS[p.id] !== undefined) journeyStatus[p.id] = p.status;

let score = 0;
for (const [id, w] of Object.entries(WEIGHTS)) {
  if (journeyStatus[id] === 'pass') score += w;
}
const max = Object.values(WEIGHTS).reduce((a, b) => a + b, 0);

const p0 = findings.filter((f) => f.severity === 'P0');
const p1 = findings.filter((f) => f.severity === 'P1');
const p2 = findings.filter((f) => f.severity === 'P2');

const ttcMs = typeof metrics.time_to_course_ms === 'number' ? metrics.time_to_course_ms : null;
const ttcOk = ttcMs !== null && ttcMs <= TTC_TARGET_MS;

let status;
if (p0.length > 0 || score < 70) status = 'BLOCK';
else if (p1.length > 0 || score < 85 || ttcMs === null) status = 'REVIEW';
else status = 'RELEASE';

const baseUrl = process.env.REALITY_BASE_URL || process.env.PREVIEW_URL || 'https://examfitde.lovable.app';

const result = {
  ts: new Date().toISOString(),
  base_url: baseUrl,
  status,
  score,
  max_score: max,
  time_to_course_ms: ttcMs,
  time_to_course_ok: ttcOk,
  time_to_course_target_ms: TTC_TARGET_MS,
  journeys: journeyStatus,
  weights: WEIGHTS,
  metrics,
  findings: { p0, p1, p2 },
  counts: { p0: p0.length, p1: p1.length, p2: p2.length, findings: findings.length },
};

fs.mkdirSync(RESULTS_DIR, { recursive: true });
fs.writeFileSync(path.join(RESULTS_DIR, 'pre-customer-reality-results.json'), JSON.stringify(result, null, 2));

const passList = Object.entries(journeyStatus)
  .filter(([, s]) => s === 'pass')
  .map(([id]) => `- ✅ ${id} (${WEIGHTS[id]} pts)`).join('\n') || '- _(none)_';
const failList = Object.entries(journeyStatus)
  .filter(([, s]) => s !== 'pass')
  .map(([id, s]) => `- ❌ ${id} (${WEIGHTS[id]} pts) — ${s}`).join('\n') || '- _(none)_';
const fmt = (f) => `- **${f.severity} / ${f.kind}** — \`${f.journey}\` ${f.route ? `\`${f.route}\`` : ''}\n  - ${f.detail}\n  ${f.fix ? `- _Fix:_ ${f.fix}` : ''}`;

const ttcLine = ttcMs === null
  ? '⚠️ TIME_TO_COURSE nicht gemessen'
  : `${ttcOk ? '✅' : '❌'} TIME_TO_COURSE = ${(ttcMs / 1000).toFixed(1)}s (Ziel ≤ ${TTC_TARGET_MS / 1000}s)`;

const md = `# Pre-Customer Reality Daily QA — ${result.ts.slice(0, 10)}

**Base URL:** ${baseUrl}
**Status:** **${status}**
**Customer Readiness Score:** **${score} / ${max}**
**${ttcLine}**

## Journeys passed
${passList}

## Journeys failed / missing
${failList}

## Findings
**Counts:** P0=${p0.length} · P1=${p1.length} · P2=${p2.length} · total=${findings.length}

### P0 (blockers)
${p0.length ? p0.map(fmt).join('\n') : '_none_'}

### P1 (trust / conversion)
${p1.length ? p1.map(fmt).join('\n') : '_none_'}

### P2 (UX friction)
${p2.length ? p2.map(fmt).join('\n') : '_none_'}

## Recommended fix order
${[...p0, ...p1, ...p2].slice(0, 10).map((f, i) => `${i + 1}. **${f.severity}** ${f.kind} → ${f.detail}`).join('\n') || '_no findings_'}

---
_Generated by scripts/pre-customer-reality-aggregate.mjs — Pre-Login Funnel Reality, not architecture._
`;

fs.writeFileSync(path.join(RESULTS_DIR, 'pre-customer-reality-report.md'), md);

console.log(`\n=== Pre-Customer Reality — ${status} (${score}/${max}) ===`);
console.log(`TTC: ${ttcMs === null ? 'n/a' : `${(ttcMs / 1000).toFixed(1)}s`}  Target: ${TTC_TARGET_MS / 1000}s`);
console.log(`P0=${p0.length} P1=${p1.length} P2=${p2.length}`);
console.log(`Report: reality-results/pre-customer-reality-report.md`);

process.exit(status === 'RELEASE' ? 0 : status === 'REVIEW' ? 1 : 2);
